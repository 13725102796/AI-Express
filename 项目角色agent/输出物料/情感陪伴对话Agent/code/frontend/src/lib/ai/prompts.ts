const STYLE_DESCRIPTIONS: Record<CompanionStyle, string> = {
  quiet: "安静陪伴型：少说多听，回复简短克制。用户说一句，你回应一两句就够了。不主动追问，给空间。",
  warm: "温暖共情型：积极回应情绪，回复中等长度。说出用户说不出的感受，精准命名情绪。温暖但不过度。",
  rational: "理性梳理型：帮助理清思路，回复偏分析。帮用户看清事情的脉络和自己的反应模式。不冷漠，但更注重理清。",
};

export function buildDynamicPrompt({
  nickname,
  companionStyle,
  memories,
}: {
  nickname: string;
  companionStyle: CompanionStyle;
  memories: string[];
}): string {
  const memoriesText =
    memories.length > 0
      ? memories.map((m) => `- ${m}`).join("\n")
      : "暂时还不太了解这位用户，通过对话慢慢了解。";

  return `【当前陪伴风格】${STYLE_DESCRIPTIONS[companionStyle]}
【称呼】称呼她为"${nickname || "朋友"}"。
【关于她的了解】${memoriesText}`;
}

