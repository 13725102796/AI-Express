/**
 * 给定农历年，返回该年的闰月号（1-12）；若无闰月则返回 0。
 *
 * 例：
 *   getLeapMonth(1995) → 8   （1995 年闰八月）
 *   getLeapMonth(1996) → 0   （1996 年无闰月）
 *   getLeapMonth(2023) → 2   （2023 年闰二月）
 */
export function getLeapMonth(year: number): number {
  if (year < 1900 || year > 2099) return 0;
  return LEAP_MONTH_TABLE[year - 1900] || 0;
}

/**
 * 判断给定农历年月是否「可能为闰月」。
 * 仅当该年闰月号正好等于该月号时返回 true（用户需选「正X月」or「闰X月」）。
 *
 * 例：
 *   isPotentiallyLeap(1995, 8)  → true   （1995 年闰八月，用户需选）
 *   isPotentiallyLeap(1995, 10) → false  （10 月不是闰月，无需选）
 */
export function isPotentiallyLeap(year: number, month: number): boolean {
  return getLeapMonth(year) === month;
}
